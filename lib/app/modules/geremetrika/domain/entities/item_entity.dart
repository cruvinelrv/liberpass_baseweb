class ItemEntity {
  final int? idItem;
  final String? description;
  final double? widthMeasure;
  final double? heightMeasure;
  final double? price;
  final String? unitMeasure;
  final double? quantity;
  final double? totalPriceValue;

  const ItemEntity({
    this.idItem,
    this.description,
    this.widthMeasure,
    this.heightMeasure,
    this.price,
    this.unitMeasure,
    this.quantity,
    this.totalPriceValue,
  });
}
